package com.iv.common;

public enum ErrorMsg implements com.iv.common.response.IErrorMsg {
	
	OK(0,"ok"),
	
	ANALYSIS_FAILED(44300, "告警分析失败");

	private int code;
    private String msg;
	
	ErrorMsg(int code , String msg){
		this.code = code;
		this.msg = msg;
	}
	
	@Override
	public int getCode() {
		return this.code;
	}

	@Override
	public void setCode(int code) {
		this.code = code;
	}

	@Override
	public String getMsg() {
		return this.msg;
	}

	@Override
	public void setMsg(String msg) {
		this.msg = msg;
	}

}
